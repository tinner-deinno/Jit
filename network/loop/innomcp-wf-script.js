var HEAD_SCHEMA = {
  type: 'object',
  properties: {
    department: { type: 'string' },
    plan: { type: 'string' },
    key_files: { type: 'array', items: { type: 'string' } },
    priority_tasks: { type: 'array', items: { type: 'string' } }
  },
  required: ['department', 'plan', 'key_files', 'priority_tasks']
}

var WORKER_SCHEMA = {
  type: 'object',
  properties: {
    task_id: { type: 'string' },
    status: { type: 'string', enum: ['done', 'blocked', 'partial'] },
    summary: { type: 'string' },
    files_changed: { type: 'array', items: { type: 'string' } }
  },
  required: ['task_id', 'status', 'summary']
}

var totalAgents = 0
var allDeptResults = {}

for (var di = 0; di < DEPTS.length; di++) {
  var dept = DEPTS[di]
  phase(dept.name)

  var head = await agent(dept.head, {
    model: 'opus',
    label: dept.name + ' Head',
    schema: HEAD_SCHEMA,
    phase: dept.name
  })
  totalAgents++
  log(dept.name + ' Head: ' + (head && head.plan ? head.plan.slice(0, 150) : 'planning...'))

  var results = await pipeline(
    dept.tasks,
    function(task, idx) {
      return agent(
        'You are a worker in ' + dept.name + ' (' + dept.ticket + ').\n' +
        'Your task: ' + task + '\n\n' +
        'Context: Completing innomcp backlog for the Jit/innomcp system.\n' +
        'Department head has planned the work.\n\n' +
        'Rules:\n' +
        '- Read relevant files before making changes\n' +
        '- Implement/Create: actually write/edit code\n' +
        '- Test/Run: execute actual command\n' +
        '- Analyze/Audit: provide detailed findings\n' +
        '- Document: create documentation file\n' +
        '- Be concise but thorough\n' +
        '- Report status: done, blocked, or partial',
        {
          label: dept.name.replace(/[^a-zA-Z0-9]/g, '') + '-' + (idx + 1),
          phase: dept.name,
          schema: WORKER_SCHEMA
        }
      )
    }
  )

  totalAgents += dept.tasks.length
  var done = results.filter(function(r) { return r && r.status === 'done' }).length
  allDeptResults[dept.name] = done + '/' + dept.tasks.length
  log(dept.name + ': ' + done + '/' + dept.tasks.length + ' done (total: ' + totalAgents + ')')
}

log('ALL DEPARTMENTS COMPLETE. Total agents: ' + totalAgents)

return {
  deployment: 'innomcp-mass-deployment',
  total_agents: totalAgents,
  departments: allDeptResults
}